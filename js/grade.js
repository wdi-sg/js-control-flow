var grade = 89;

switch (true) {
  case grade >= 100 :
    console.log("A");
    break;
  case grade >= 80:
    console.log("B");
    break;
  case grade >= 50:
    console.log("C");
    break;
  case grade >= 40:
    console.log("D");
    break;
  case grade >= 30:
    console.log("F");
    break;
  default:
    console.log("You have entered invalid value");

}
